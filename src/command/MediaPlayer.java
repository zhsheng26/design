package command;

public class MediaPlayer {
    public void insertCD() {
        System.out.println("放入CD到播放器");
    }

    public void play() {
        System.out.println("开始播放");
    }

    public void stop() {
        System.out.println("停止播放");
    }

    public void close() {
        System.out.println("关闭播放器");
    }


}
